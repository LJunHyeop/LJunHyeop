//p.132 대입연산자
public class AssignmentOperator {
    public static void main(String[] args) {
        int x, y;  // left value(저장할 수 있는 공간 ),
        // right value (연산식, 변수 , 리터럴)
        x = y = 3;
        //y = 3;
        //x = y;
        System.out.println(x);
        System.out.println(y);
    }
}
