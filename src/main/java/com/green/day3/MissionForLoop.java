package com.green.day3;

public class MissionForLoop {
    public static void main(String[] args) {
        //1~100까지 숫자를 세로로 출력 index=0으로 세팅
        for (int i = 0; i <= 99; i++) {
            System.out.println(i + 1); // int i = 0 for(; i<100;  ) {
            // System.out.println(i+1)
            // i++; 해도 나오긴함}
        }
    }
}
