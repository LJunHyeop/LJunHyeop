package com.green.day8.ch4;

public class FlowEx12 {
    public static void main(String[] args) {
        /*
        반복물을 사용하여 1~5를 세로로 출력한다.

        반복문을 사용하여 1~5를 가로로 출력한다.
         */
        int i = 0;
        int z = 0;
        for (i = 1; i < 6; i++) {
            System.out.println(i);
        }
        for (z = 1; z < 6; z++) {
            System.out.print(z);
        }
    }
}
